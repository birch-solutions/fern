    {
        try {
            $response = $this->client->sendRequest(
                new JsonApiRequest(
                    baseUrl: $this->options['baseUrl'] ?? '',
                    path: "/problems-v2/lightweight-problem-info",
                    method: HttpMethod::GET,
                ),
            );
            $statusCode = $response->getStatusCode();
